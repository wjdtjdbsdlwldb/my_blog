      <p class="column-tit">
        <a class="block" href="#">스마트 디바이스의 세대별 사용자 경험 변화 연구</a></p>
      <p class="column-sub">산업이 발전하면서 경험은 비즈니스에서 가장 중요한 요소로 꼽히고 있다. 조셉 파인과 길모어(Joseph Pine & Gilmore, J. H., 1998)는 현재는 경험경제(Experience Economic)시대라고 주장하며 상품을 팔고 서비스를 파는 시대를 지나서 경험을 파는 시대가 왔다고 하였다[1]. 현재는 복잡하고 어려운 기술 기반의 제품과 서비스 자체가 아닌 그것을 사용하면서 느끼는 경험의 가치가 향상된 시대인 것이다. 국제표준화기구의 사용자 경험(User Experience) 정의는 제품, 시스템 또는 서비스를 사용하거나, 사용하도록 만드는 사용자의 지각과 반응[2]이라 하였다. 또한, 닐슨 노먼 그룹(Nielsen Norman Group)은 사용자와 회사, 제품, 서비스의 상호작용 모든 것을 아우르는 것[3]이라 정의하고 있다. 즉, 사용자 경험은 어떤 제품 혹은 서비스를 직·간접적으로 이용하며 생각하고 느끼는 총체적인 경험이라 할 수 있다. 그리고 사용자 경험의 개념은 1970년대 말 인간과 컴퓨터 상호작용을 일컫는 HCI(Human-Computer Interaction)분야로부터 시작되었다[4]. HCI가 최종적으로 달성하고자 하는 목표 역시 사람들이 디지털 제품이나 서비스를 이용해 작업을 수행하거나 문제를 해결하는 과정에서 그들에게 최적의 경험을 제공하는 것이다[5].(Csikszentmihalyi, 1998)</p>
